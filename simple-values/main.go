package main

import "fmt"

func main() {

	//strings
	fmt.Println("hello world")
	fmt.Println("hello world + 123")

	//number
	fmt.Println(123)

	//float
	fmt.Println(2.4)
	fmt.Println(88.94)

	//bool
	fmt.Println(true)
	fmt.Println(false)

	//simple math
	fmt.Println(3 + 4)
	fmt.Println(3 - 88)
	fmt.Println(3 / 4)
	fmt.Println(3 * 4)

	// precedance of operators
	fmt.Println(3 + 4*4)
	fmt.Println(3/3*4 - 4 + 2%2)

}
